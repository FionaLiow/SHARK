<p>File upload vulnerability is a security flaw that allows attackers to upload malicious files to a server. In simple terms, it's like sneaking dangerous files past a security checkpoint, potentially causing harm to the system.</p>
</br>
<p><strong>Types of File Upload Vulnerabilities:</strong></p>
<p><strong>Unrestricted Upload: </strong>No validation on uploaded files.</p>
<p><strong>Content-Type Mismatch: </strong>Only checks MIME type, not actual content.</p>
<p><strong>File Extension Mismatch: </strong>Checks extension but not content.</p>
<p><strong>Path Traversal: </strong>Files saved in public directories.</p>
<p><strong>Overwriting Files: </strong>Uploaded files overwrite critical existing files.</p>
</br>
<p><strong>Common Exploits:</strong></p>
<p><strong>Remote Code Execution (RCE) : </strong>Executing malicious scripts on the server.</p>
<p><strong>Defacement: </strong>Uploading HTML files to alter a website.</p>
<p><strong>Storage Consumption: </strong>Uploading large files to fill server storage.</p>
<p><strong>Malware Distribution: </strong>Uploading files that users download and run.</p>
